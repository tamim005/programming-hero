// while loop
 var roastGiven = 0;
while(roastGiven < 7){
    console.log('Roast den, Please!!')
    roastGiven++;
} 

// for loop
 for(var roastGiven = 0; roastGiven < 7 ; roastGiven++){
    console.log('Roast den, Please !!');
} 

// for loop simple version
 for(var i = 0; i < 7; i++){
    console.log("roast den, please");
} 


//display 1 to 10 with for loop
for(var i = 1; i <= 10; i++){
    console.log(i);
}

//display 1 to 10 even number with for loop
for(var i = 0; i <= 10; i += 2){
    console.log(i);
}

// display 1 to 10 odd number with for loop
