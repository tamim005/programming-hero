// string concatenation
var firstName = 'Muhammad';
var lastName = 'Tamim';
var fullName = firstName + ' ' + lastName;
console.log(fullName);

// convert string to number with parseInt
var price1 = '31';
var price2 = 20;
price1 = parseInt(price1);
console.log(price1 + price2);

// convert string to float number with parseFloat
var jscGpa = '3.41';
var sscGpa = '4.89';
jscGpa = parseFloat(jscGpa);
sscGpa = parseFloat(sscGpa);
var sum = jscGpa + sscGpa;
console.log(sum);